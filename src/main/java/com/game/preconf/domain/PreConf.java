package com.game.preconf.domain;

public class PreConf {
    private int id;
    private String gameId;
    private int net;

    public int getId() {
        return id;
    }

    public String getGameId() {
        return gameId;
    }

    public int getNet() {
        return net;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setGameId(String gameId) {
        this.gameId = gameId;
    }

    public void setNet(int net) {
        this.net = net;
    }
}
