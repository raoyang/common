package com.game.gconf.domain;

public class GConf {
    private int type;
    private String value;

    public void setType(int type) {
        this.type = type;
    }

    public int getType() {
        return type;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
